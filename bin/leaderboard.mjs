#!/usr/bin/env node
/**
 * Comparative leaderboard: reads every measured adapter coverage.json + the rubric, and renders a
 * per-vector × per-tool matrix, per-tool totals, and the defense-in-depth (complementarity) view.
 *
 * Only adapters with measuredBy (i.e. produced by bin/run.mjs) are included — self-reported claims
 * are excluded so the board reflects verified results.  Usage: node bin/leaderboard.mjs
 */
import { readFileSync, readdirSync, existsSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const root = join(here, "..");
const rubric = JSON.parse(readFileSync(join(root, "rubric", "crosswalk.json"), "utf8"));
const ids = rubric.vectors.map((v) => v.id);
const WEIGHT = { enforce: 1, detect: 0.5, none: 0, unknown: 0 };
const glyph = { enforce: "🟢", detect: "🟡", none: "·", unknown: "?" };

const tools = [];
for (const d of readdirSync(join(root, "adapters"))) {
  const p = join(root, "adapters", d, "coverage.json");
  if (!existsSync(p)) continue;
  const c = JSON.parse(readFileSync(p, "utf8"));
  if (!c.measuredBy) continue; // verified results only
  tools.push(c);
}
tools.sort((a, b) => a.tool.localeCompare(b.tool));

const lvl = (c, id) => c.coverage?.[id]?.level ?? "unknown";
const weighted = (c) => ids.reduce((a, id) => a + (WEIGHT[lvl(c, id)] ?? 0), 0);

const L = [];
L.push("# Leaderboard (verified)", "");
L.push(`_Generated by \`bin/leaderboard.mjs\` from measured runs. Rubric v${rubric.version}, ${ids.length} vectors._`, "");

// Totals
L.push("## Coverage of the full attack surface", "");
L.push("| Tool | Class | Weighted coverage | 🟢 enforce | 🟡 detect | · none |");
L.push("|---|---|--:|--:|--:|--:|");
for (const c of tools) {
  const w = weighted(c);
  const n = { enforce: 0, detect: 0, none: 0, unknown: 0 };
  for (const id of ids) n[lvl(c, id)] = (n[lvl(c, id)] ?? 0) + 1;
  L.push(
    `| **${c.tool}** | ${c.class ?? "?"} | ${((w / ids.length) * 100).toFixed(0)}% (${w.toFixed(1)}/${ids.length}) | ${n.enforce} | ${n.detect} | ${n.none} |`,
  );
}
L.push("");

// Matrix
L.push("## Per-vector matrix", "");
L.push(`| Vector | Layer | ${tools.map((t) => t.tool).join(" | ")} |`);
L.push(`|---|---|${tools.map(() => "--").join("|")}|`);
for (const v of rubric.vectors) {
  L.push(`| ${v.name} | ${v.mcpLayer[0]} | ${tools.map((t) => glyph[lvl(t, v.id)]).join(" | ")} |`);
}
L.push("");

// Defense-in-depth
const anyCovers = (id) => tools.some((t) => ["detect", "enforce"].includes(lvl(t, id)));
const covered = ids.filter(anyCovers);
const uncovered = ids.filter((id) => !anyCovers(id));
L.push("## Defense-in-depth view", "");
L.push(`- **Covered by at least one tool:** ${covered.length}/${ids.length}`);
L.push(`- **Covered by NO measured tool:** ${uncovered.length}/${ids.length} — ${uncovered.join(", ") || "none"}`);
// Complementarity: vectors where tools differ
const complementary = covered.filter((id) => {
  const levels = tools.map((t) => lvl(t, id));
  return new Set(levels.filter((x) => x !== "none" && x !== "unknown")).size >= 1 && levels.includes("none");
});
L.push(
  `- **Complementarity:** ${complementary.length} vectors are caught by some tools but missed by others — evidence that no single proxy suffices.`,
);
L.push("");
L.push("> **Caveat:** absolute scores are corpus-dependent. This corpus (v0.1) is authored at the tool-definition and tool-call layers; it under-probes egress/secret-exfiltration encodings where network-firewall-class tools are strongest. Read the matrix for *which* vectors each tool covers, not just the totals. See `docs/ADAPTERS.md`.");

process.stdout.write(L.join("\n") + "\n");
